public class PrettyPrinting {
    public static void main(String[] args) {
        float a = 45.5612f;
        System.out.printf("Formated number is %.2f ", a);
        System.out.println();
        System.out.println(Math.PI);

        System.out.printf("Formated number of Math.PI is %.3f", Math.PI);
        System.out.println();

        System.out.printf("Hello my name is %s and I am %s", "Ashish", "Op");
    }    
}