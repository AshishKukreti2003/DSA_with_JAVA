public class Loops {
    public static void main(String[] args){
        // print number form 1 to 5
        // for loops
        for(int i=1;i<=5;i++){
            System.out.println(i);
        }

        // while loops
        int a = 1;
        while(a!=6){
            System.out.println(a);
            a++;
        }

        // do-while loop
        int num = 1;
        do{
            System.out.println(num);
            num++;
        }while(num<=5);

    
    }
}
